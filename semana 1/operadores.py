# Quantidade de aulas de dança por semana
dani = 5
debora = 3
soma = dani + debora

# Operadores aritméticos
print("Dani + Debora = " + str(soma) )
print("Dani + Debora = " , soma)
print(f"Dani + Débora = {soma}")   # Soma: 5 + 3 = 8 aulas no total

print(f"Dani - Débora = {dani - debora}")   # Subtração: 5 - 3 = 2 aulas a mais
print(f"Dani * Débora = {dani * debora}")   # Multiplicação: 5 * 3 = 15 combinações de treinos
print(f"Dani / Débora = {dani / debora}")   # Divisão: 5 / 3 ≈ 1.66 vezes mais aulas
print(f"Dani // Débora = {dani // debora}") # Divisão inteira: 5 // 3 = 1 semana completa
print(f"Dani % Débora = {dani % debora}")   # Resto da divisão: 5 % 3 = 2 aulas sobrando
print(f"Dani ** Débora = {dani ** debora}") # Potência: 5 ** 3 = 125 sequências possíveis

# Operadores relacionais (comparações)
print(f"Dani faz mais aulas que Débora? {dani >=debora}")      # True
print(f"Dani faz menos aulas que Débora? {dani <= debora}")     # False
print(f"Dani faz a mesma quantidade que Débora? {dani == debora}")  # False
print(f"Dani e Debora faz quantidade diferente ? {dani != debora}")  # True

