#Usar range() para simular o envio de convites para um evento.
print("Enviando convites para 5 participantes:")
for i in range(5):
    print(f"Convite enviado para o participante {i + 1}")

#Se tivermos 10 participantes, qual número você mudaria no range()?


#Simular o cálculo de salários para funcionários de número 101 a 105.
print("Calculando salários de funcionários:")
for matricula in range(101, 106):
    print(f"Calculando salário do funcionário de matrícula {matricula}")
#Se o primeiro funcionário tiver matrícula 500 e o último 510, como ficaria o range()?

#Mostrar apenas os meses pares para geração de relatórios financeiros.

print("Gerando relatórios dos meses pares:")
for mes in range(2, 13, 2):
    print(f"Relatório do mês {mes} gerado com sucesso!")
#Se eu quiser relatórios trimestrais em vez de mensais, qual valor devo mudar no range()?