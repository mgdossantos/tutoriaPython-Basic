# Create a variable with the number 10 and another with the number 5.
# Add, subtract, multiply, and divide these numbers, then display the results.

a = 10
b = 5

# Addition
r = a + b
print("Sum:", r)

# Subtraction
r = a - b
print("Subtraction:", r)

# Multiplication
r = a * b
print("Multiplication:", r)

# Division
r = a / b
print("Division:", r)
