# Number of dance classes per week
dani = 5
debora = 3

# Arithmetic operators
print(f"Dani + Débora = {dani + debora}")    # Addition: 5 + 3 = 8 total classes
print(f"Dani - Débora = {dani - debora}")    # Subtraction: 5 - 3 = 2 more classes
print(f"Dani * Débora = {dani * debora}")    # Multiplication: 5 * 3 = 15 possible training combinations
print(f"Dani / Débora = {dani / debora}")    # Division: 5 / 3 ≈ 1.66 times more classes
print(f"Dani // Débora = {dani // debora}")  # Floor division: 5 // 3 = 1 complete week
print(f"Dani % Débora = {dani % debora}")    # Modulus: 5 % 3 = 2 remaining classes
print(f"Dani ** Débora = {dani ** debora}")  # Exponentiation: 5 ** 3 = 125 possible sequences

# Relational operators (comparisons)
print(f"Does Dani attend more classes than Débora? {dani > debora}")      # True
print(f"Does Dani attend fewer classes than Débora? {dani < debora}")     # False
print(f"Does Dani attend the same number of classes as Débora? {dani == debora}")  # False

